CREATE TABLE "Infantes" (
    "Id" uuid NOT NULL,
    "Nombre" character varying(100) NOT NULL,
    "Apellido" character varying(100) NOT NULL,
    "Documento" character varying(50),
    "PhotoUrl" text,
    "FechaNacimiento" timestamp with time zone NOT NULL,
    CONSTRAINT "PK_Infantes" PRIMARY KEY ("Id")
);

CREATE TABLE "Jardines" (
    "Id" uuid NOT NULL,
    "Nombre" character varying(100) NOT NULL,
    CONSTRAINT "PK_Jardines" PRIMARY KEY ("Id")
);

CREATE TABLE "Roles" (
    "Id" integer GENERATED BY DEFAULT AS IDENTITY,
    "Descripcion" character varying(200) NOT NULL,
    CONSTRAINT "PK_Roles" PRIMARY KEY ("Id")
);

CREATE TABLE "RolesSala" (
    "Id" integer GENERATED BY DEFAULT AS IDENTITY,
    "Descripcion" character varying(200) NOT NULL,
    CONSTRAINT "PK_RolesSala" PRIMARY KEY ("Id")
);

CREATE TABLE "TiposTutelas" (
    "Id" integer GENERATED BY DEFAULT AS IDENTITY,
    "Descripcion" character varying(50) NOT NULL,
    CONSTRAINT "PK_TiposTutelas" PRIMARY KEY ("Id")
);

CREATE TABLE "Salas" (
    "Id" uuid NOT NULL,
    "Nombre" character varying(100) NOT NULL,
    "IdJardin" uuid NOT NULL,
    CONSTRAINT "PK_Salas" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_Salas_Jardines_IdJardin" FOREIGN KEY ("IdJardin") REFERENCES "Jardines" ("Id") ON DELETE CASCADE
);

CREATE TABLE "Usuarios" (
    "Id" uuid NOT NULL,
    "Email" character varying(254) NOT NULL,
    "PasswordHash" character varying(500) NOT NULL,
    "FechaAlta" timestamp with time zone NOT NULL,
    "FechaModificacion" timestamp with time zone NOT NULL,
    "FechaBaja" timestamp with time zone,
    "IdRol" integer NOT NULL,
    "IdJardin" uuid,
    "CaracteristicaPais" character varying(3),
    "CodigoArea" character varying(6),
    "Numero" character varying(20),
    CONSTRAINT "PK_Usuarios" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_Usuarios_Jardines_IdJardin" FOREIGN KEY ("IdJardin") REFERENCES "Jardines" ("Id"),
    CONSTRAINT "FK_Usuarios_Roles_IdRol" FOREIGN KEY ("IdRol") REFERENCES "Roles" ("Id") ON DELETE CASCADE
);

CREATE TABLE "Personas" (
    "IdUsuario" uuid NOT NULL,
    "Nombre" character varying(100) NOT NULL,
    "Apellido" character varying(100) NOT NULL,
    "Documento" character varying(50),
    "PhotoUrl" text,
    CONSTRAINT "PK_Personas" PRIMARY KEY ("IdUsuario"),
    CONSTRAINT "FK_Personas_Usuarios_IdUsuario" FOREIGN KEY ("IdUsuario") REFERENCES "Usuarios" ("Id") ON DELETE CASCADE
);

CREATE TABLE "Tutelas" (
    "IdInfante" uuid NOT NULL,
    "IdUsuario" uuid NOT NULL,
    "IdTipoTutela" integer NOT NULL,
    "EsPrincipal" boolean NOT NULL,
    "FechaAlta" timestamp with time zone NOT NULL,
    CONSTRAINT "PK_Tutelas" PRIMARY KEY ("IdInfante", "IdUsuario"),
    CONSTRAINT "FK_Tutelas_Infantes_IdInfante" FOREIGN KEY ("IdInfante") REFERENCES "Infantes" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_Tutelas_TiposTutelas_IdTipoTutela" FOREIGN KEY ("IdTipoTutela") REFERENCES "TiposTutelas" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_Tutelas_Usuarios_IdUsuario" FOREIGN KEY ("IdUsuario") REFERENCES "Usuarios" ("Id") ON DELETE CASCADE
);

CREATE TABLE "Usuarios_Salas_Roles" (
    "IdUsuario" uuid NOT NULL,
    "IdSala" uuid NOT NULL,
    "IdRolSala" integer NOT NULL,
