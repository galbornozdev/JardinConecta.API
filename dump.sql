    CONSTRAINT "FK_Usuarios_Salas_Roles_RolesSala_IdRolSala" FOREIGN KEY ("IdRolSala") REFERENCES "RolesSala" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_Usuarios_Salas_Roles_Salas_IdSala" FOREIGN KEY ("IdSala") REFERENCES "Salas" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_Usuarios_Salas_Roles_Usuarios_IdUsuario" FOREIGN KEY ("IdUsuario") REFERENCES "Usuarios" ("Id") ON DELETE CASCADE
);

